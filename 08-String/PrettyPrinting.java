// 7. Pretty Printing (Formatted Output)
public class PrettyPrinting {
    public static void main(String[] args) {
        System.out.printf("Formatted number: %.7f\n", 453.1274);
        System.out.printf("Pi: %.7f\n", Math.PI);
        System.out.printf("Hello, my name is %s and I am %s\n", "Abhishek", "Cool");
    }
}