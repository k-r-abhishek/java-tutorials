/*
 * 4. Type Casting
 */
public class TypeCasting {
    public static void main(String[] args) {
        int num = (int) 67.854f; // Explicit Casting
        double value = 100; // Implicit Casting
        
        System.out.println("Explicit Casting: " + num);
        System.out.println("Implicit Casting: " + value);
    }
}