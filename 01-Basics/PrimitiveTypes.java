/*
 * 3. Primitive Data Types
 */
public class PrimitiveTypes {
    public static void main(String[] args) {
        byte smallNumber = 100;
        short shortNumber = 32000;
        int integerNumber = 50000;
        long largeNumber = 9876543210L;
        float decimalNumber = 5.75f;
        double largeDecimalNumber = 19.99;
        char letter = 'J';
        boolean isCodingFun = true;
        
        System.out.println("Byte: " + smallNumber);
        System.out.println("Short: " + shortNumber);
        System.out.println("Integer: " + integerNumber);
        System.out.println("Long: " + largeNumber);
        System.out.println("Float: " + decimalNumber);
        System.out.println("Double: " + largeDecimalNumber);
        System.out.println("Char: " + letter);
        System.out.println("Boolean: " + isCodingFun);
    }
}
