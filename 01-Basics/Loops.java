/*
 * 7. Loops: while and for
 */
public class Loops {
    public static void main(String[] args) {
        System.out.println("While Loop Example:");
        int i = 1;
        while (i <= 5) {
            System.out.println("Count: " + i);
            i++;
        }
        
        System.out.println("For Loop Example:");
        for (int j = 1; j <= 5; j++) {
            System.out.println("Iteration: " + j);
        }
    }
}